// ฟังก์ชันสำหรับค้นหาข้อมูลจากคำถาม
export const findMedicalInfo = (userMessage) => {
  const message = userMessage.toLowerCase().trim();
  
  // ค้นหาอาการ
  for (const [symptom, info] of Object.entries(medicalDatabase.symptoms)) {
    if (message.includes(symptom.toLowerCase())) {
      return {
        type: 'symptom',
        symptom: symptom,
        ...info
      };
    }
  }

  // ค้นหาคำถามทั่วไป
  for (const [keyword, info] of Object.entries(medicalDatabase.generalQuestions)) {
    if (message.includes(keyword.toLowerCase())) {
      return {
        type: 'general',
        keyword: keyword,
        ...info
      };
    }
  }

  // ค้นหาแผนก
  for (const [dept, info] of Object.entries(medicalDatabase.departments)) {
    if (message.includes(dept.toLowerCase())) {
      return {
        type: 'department',
        department: dept,
        ...info
      };
    }
  }

  return null;
};

// ฟังก์ชันสำหรับค้นหาแพทย์ตามแผนกและอาการ
export const findDoctors = (department, symptom = null) => {
  const doctors = medicalDatabase.doctors[department];
  if (!doctors) return [];

  // ถ้ามีอาการ ให้หาแพทย์ที่เชี่ยวชาญในอาการนั้น
  if (symptom) {
    return doctors.filter(doctor => 
      doctor.expertise.some(exp => 
        exp.toLowerCase().includes(symptom.toLowerCase()) ||
        symptom.toLowerCase().includes(exp.toLowerCase())
      )
    );
  }

  return doctors;
};

// ฟังก์ชันสำหรับสร้างคำตอบ
export const generateResponse = (userMessage) => {
  const info = findMedicalInfo(userMessage);
  
  if (!info) {
    return {
      message: 'ขออภัยค่ะ ฉันไม่เข้าใจคำถามของคุณ กรุณาลองถามใหม่หรืออธิบายอาการที่คุณกำลังประสบอยู่ค่ะ',
      showDoctor: false,
      doctors: []
    };
  }

  if (info.type === 'symptom') {
    const recommendedDoctors = findDoctors(info.department, info.symptom);
    const doctorsToShow = recommendedDoctors.length > 0 ? recommendedDoctors : findDoctors(info.department);
    
    let doctorInfo = '';
    if (doctorsToShow.length > 0) {
      doctorInfo = '\n\nแพทย์ที่แนะนำ:\n';
      doctorsToShow.slice(0, 2).forEach((doctor, index) => {
        doctorInfo += `${index + 1}. ${doctor.name}\n   เชี่ยวชาญ: ${doctor.specialization}\n   ประสบการณ์: ${doctor.experience}\n\n`;
      });
    }

    return {
      message: `${info.description}\n\nคำแนะนำ: ${info.advice}\n\nแนะนำให้พบ${info.doctorType}ที่${info.department}เพื่อรับการตรวจวินิจฉัยที่ถูกต้องค่ะ${doctorInfo}`,
      showDoctor: true,
      department: info.department,
      doctorType: info.doctorType,
      doctors: doctorsToShow
    };
  }

  if (info.type === 'general') {
    return {
      message: info.answer,
      showDoctor: false,
      doctors: []
    };
  }

  if (info.type === 'department') {
    const departmentDoctors = findDoctors(info.department);
    
    let doctorInfo = '';
    if (departmentDoctors.length > 0) {
      doctorInfo = '\n\nแพทย์ในแผนก:\n';
      departmentDoctors.forEach((doctor, index) => {
        doctorInfo += `${index + 1}. ${doctor.name}\n   เชี่ยวชาญ: ${doctor.specialization}\n   ประสบการณ์: ${doctor.experience}\n\n`;
      });
    }

    return {
      message: `${info.description}\n\nแผนกนี้ดูแลรักษา: ${info.commonIssues.join(', ')}\n\nหากคุณมีอาการเหล่านี้ สามารถนัดหมายแพทย์ได้ที่แผนกนี้ค่ะ${doctorInfo}`,
      showDoctor: true,
      department: info.department,
      doctors: departmentDoctors
    };
  }

  return {
    message: 'ขออภัยค่ะ ฉันไม่เข้าใจคำถามของคุณ กรุณาลองถามใหม่ค่ะ',
    showDoctor: false,
    doctors: []
  };
};

// ฟังก์ชันสำหรับค้นหาแพทย์โดยตรง
export const searchDoctors = (query) => {
  const lowerQuery = query.toLowerCase();
  const results = [];

  for (const [department, doctors] of Object.entries(medicalDatabase.doctors)) {
    doctors.forEach(doctor => {
      if (
        doctor.name.toLowerCase().includes(lowerQuery) ||
        doctor.specialization.toLowerCase().includes(lowerQuery) ||
        doctor.expertise.some(exp => exp.toLowerCase().includes(lowerQuery))
      ) {
        results.push({ ...doctor, department });
      }
    });
  }

  return results;
};

